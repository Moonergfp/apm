package apm.modules.terminal.entity;

import apm.common.utils.JSONUtils;

/**
 * 终端信息
 * @author gfp
 *
 */
public class TerminalInfo {
	private String terminalId;	//终端ID
	private String status;		//状态（0：离线，1：在线）
	private String mem;			//总内存
	private String usedMem;	//剩余内存
	private String avalialbeMem;//可用内存
	private String programId;	//当前正在播放的节目
	
	
	public String getProgramId() {
		return programId;
	}
	public void setProgramId(String programId) {
		this.programId = programId;
	}
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMem() {
		return mem;
	}
	public void setMem(String mem) {
		this.mem = mem;
	}
	public String getAvalialbeMem() {
		return avalialbeMem;
	}
	public void setAvalialbeMem(String avalialbeMem) {
		this.avalialbeMem = avalialbeMem;
	}
	
	public String getUsedMem() {
		return usedMem;
	}
	public void setUsedMem(String usedMem) {
		this.usedMem = usedMem;
	}
	@Override
	public String toString() {
		return "TerminalInfo [status=" + status + ", mem=" + mem
				+ ", usedMem=" + usedMem + ", avalialbeMem=" + avalialbeMem
				+ "]";
	}
}
